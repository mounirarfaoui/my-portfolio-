/* ─── HAMBURGER ─── */
const hamburger  = document.getElementById("hamburger");
const mobileMenu = document.getElementById("mobileMenu");

hamburger.addEventListener("click", () => {
  hamburger.classList.toggle("open");
  mobileMenu.classList.toggle("open");
});

mobileMenu.querySelectorAll("a").forEach(a => {
  a.addEventListener("click", () => {
    hamburger.classList.remove("open");
    mobileMenu.classList.remove("open");
  });
});

/* ─── PORTFOLIO FILTER ─── */
const filterBtns = document.querySelectorAll(".filter-btn");
const workCards  = document.querySelectorAll(".work-card");

function playVisibleAdVideos() {
  document.querySelectorAll('.work-card[data-type="ad"]:not(.hidden) video').forEach(video => {
    video.play().catch(() => {});
  });
}

document.querySelectorAll('.work-card[data-type="ad"] video').forEach(video => {
  const card = video.closest(".work-card");
  const io = new IntersectionObserver(
    entries => {
      entries.forEach(entry => {
        if (!entry.isIntersecting || card.classList.contains("hidden")) {
          video.pause();
          return;
        }
        video.play().catch(() => {});
      });
    },
    { threshold: 0.15, rootMargin: "0px 0px -8% 0px" }
  );
  io.observe(card);
});

filterBtns.forEach(btn => {
  btn.addEventListener("click", () => {
    filterBtns.forEach(b => b.classList.remove("active"));
    btn.classList.add("active");

    const filter = btn.dataset.filter;
    workCards.forEach(card => {
      const match = filter === "all" || card.dataset.type === filter;
      card.classList.toggle("hidden", !match);
      if (match) card.classList.add("visible");
    });

    queueMicrotask(playVisibleAdVideos);
  });
});

/* ─── SCROLL FADE IN ─── */
const fadeEls = document.querySelectorAll(
  ".service-card, .about-grid, .stat, .tool-chip"
);

fadeEls.forEach(el => el.classList.add("fade-in"));

const observer = new IntersectionObserver(entries => {
  entries.forEach(e => {
    if (e.isIntersecting) {
      e.target.classList.add("visible");
      observer.unobserve(e.target);
    }
  });
}, { threshold: 0.1 });

fadeEls.forEach(el => observer.observe(el));

/* ─── CONTACT FORM ─── */
document.getElementById("contactForm").addEventListener("submit", e => {
  e.preventDefault();
  const btn = e.target.querySelector("button[type=submit]");
  const orig = btn.innerHTML;
  btn.innerHTML = currentLang === "ar" ? "✓ تم الإرسال!" : currentLang === "fr" ? "✓ Envoyé !" : "✓ Sent!";
  btn.style.background = "linear-gradient(135deg, #16a34a, #15803d)";
  setTimeout(() => {
    btn.innerHTML = orig;
    btn.style.background = "";
    e.target.reset();
  }, 3000);
});

/* ─── SMOOTH SCROLL (mobile menu close) ─── */
document.querySelectorAll('a[href^="#"]').forEach(a => {
  a.addEventListener("click", e => {
    const target = document.querySelector(a.getAttribute("href"));
    if (!target) return;
    e.preventDefault();
    const top = target.getBoundingClientRect().top + window.scrollY - 68;
    window.scrollTo({ top, behavior: "smooth" });
  });
});
